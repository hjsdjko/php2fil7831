<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				canguanbianhao: this.getUUID(),
				wenwumingcheng: '',
				wenwuleibie: '',
				tupian: '',
				didian: '',
			        jf: '50',
				dakashijian: '',
				yonghuzhanghao: '',
				xingming: '',
				ganshoufenxiang: '',
				},
				// 登录用户信息
				user: {},
                ro:{
                   canguanbianhao : false,
                   wenwumingcheng : false,
                   wenwuleibie : false,
                   tupian : false,
                   didian : false,
                   jf : false,
                   dakashijian : false,
                   yonghuzhanghao : false,
                   xingming : false,
                   ganshoufenxiang : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
            this.ruleForm.dakashijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.yonghuzhanghao = this.user.yonghuzhanghao
			this.ro.yonghuzhanghao = true;
			this.ruleForm.xingming = this.user.xingming
			this.ro.xingming = true;



			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`wenwuzhilv`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='canguanbianhao'){
					this.ruleForm.canguanbianhao = obj[o];
					this.ro.canguanbianhao = true;
					continue;
					}
					if(o=='wenwumingcheng'){
					this.ruleForm.wenwumingcheng = obj[o];
					this.ro.wenwumingcheng = true;
					continue;
					}
					if(o=='wenwuleibie'){
					this.ruleForm.wenwuleibie = obj[o];
					this.ro.wenwuleibie = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o].split(",")[0];
					this.ro.tupian = true;
					continue;
					}
					if(o=='didian'){
					this.ruleForm.didian = obj[o];
					this.ro.didian = true;
					continue;
					}
					if(o=='jf'){
					this.ruleForm.jf = obj[o];
					this.ro.jf = true;
					continue;
					}
					if(o=='dakashijian'){
					this.ruleForm.dakashijian = obj[o];
					this.ro.dakashijian = true;
					continue;
					}
					if(o=='yonghuzhanghao'){
					this.ruleForm.yonghuzhanghao = obj[o];
					this.ro.yonghuzhanghao = true;
					continue;
					}
					if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					this.ro.xingming = true;
					continue;
					}
					if(o=='ganshoufenxiang'){
					this.ruleForm.ganshoufenxiang = obj[o];
					this.ro.ganshoufenxiang = true;
					continue;
					}
				}
			}
			this.styleChange()
            this.$forceUpdate()
		},
		methods: {
            ganshoufenxiangChange(e) {
                this.ruleForm.ganshoufenxiang = e
            },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数


			// 日长控件选择日期时间
			dakashijianConfirm(val) {
				console.log(val)
				this.ruleForm.dakashijian = val.result;
				this.$forceUpdate();
			},


			tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {




















//跨表计算判断
				var obj;
				if(this.ruleForm.jf&&(!this.$validate.isNumber(this.ruleForm.jf))){
					this.$utils.msg(`积分应输入数字`);
					return
				}
				//更新跨表属性
			       var crossuserid;
			       var crossrefid;
			       var crossoptnum;
				if(this.cross){
                    uni.setStorageSync('crossCleanType',true);
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('userid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`wenwuzhilv`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
                        uni.removeStorageSync('crossCleanType');
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`wenwuzhilv`, this.ruleForm);
						}else{
							await this.$api.add(`wenwuzhilv`, this.ruleForm);
                         if(this.user.jf>=0) {
                             this.user.jf = parseFloat(this.user.jf) + parseFloat(this.ruleForm.jf)
                             await this.$api.update(uni.getStorageSync("nowTable"), this.user);
                         }
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`wenwuzhilv`, this.ruleForm);
					}else{
						await this.$api.add(`wenwuzhilv`, this.ruleForm);
                         if(this.user.jf>=0) {
                             this.user.jf = parseFloat(this.user.jf) + parseFloat(this.ruleForm.jf)
                             await this.$api.update(uni.getStorageSync("nowTable"), this.user);
                         }
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				if(this.ro[str]){
					return false
				}
				this.$refs[str].show();
			}
		}
	}
</script>

